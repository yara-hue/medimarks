import sharp from "sharp";
import { readdirSync, existsSync, writeFileSync } from "fs";
import { join } from "path";

const ROOT = "public/images/products";
const EDGE = 3;

function walk(dir) {
  const entries = readdirSync(dir, { withFileTypes: true });
  let files = [];
  for (const e of entries) {
    const full = join(dir, e.name);
    if (e.isDirectory()) files = files.concat(walk(full));
    else if (/\.(jpe?g|png)$/i.test(e.name)) files.push(full);
  }
  return files;
}

function avg(buf) {
  let r = 0, g = 0, b = 0, n = 0;
  for (let i = 0; i < buf.length; i += 4) {
    r += buf[i]; g += buf[i + 1]; b += buf[i + 2]; n++;
  }
  return { r: Math.round(r / n), g: Math.round(g / n), b: Math.round(b / n) };
}

async function getEdgeColor(file) {
  const { width: w, height: h } = await sharp(file).metadata();
  const e = Math.min(EDGE, w, h);

  const top = await sharp(file).extract({ left: 0, top: 0, width: w, height: e }).ensureAlpha().raw().toBuffer();
  const bot = await sharp(file).extract({ left: 0, top: h - e, width: w, height: e }).ensureAlpha().raw().toBuffer();
  const left = await sharp(file).extract({ left: 0, top: e, width: e, height: h - 2 * e }).ensureAlpha().raw().toBuffer();
  const right = await sharp(file).extract({ left: w - e, top: e, width: e, height: h - 2 * e }).ensureAlpha().raw().toBuffer();

  const c1 = avg(top), c2 = avg(bot), c3 = avg(left), c4 = avg(right);
  const r = Math.round((c1.r + c2.r + c3.r + c4.r) / 4);
  const g = Math.round((c1.g + c2.g + c3.g + c4.g) / 4);
  const b = Math.round((c1.b + c2.b + c3.b + c4.b) / 4);
  return "#" + [r, g, b].map((c) => c.toString(16).padStart(2, "0")).join("");
}

async function main() {
  if (!existsSync(ROOT)) { console.error("not found"); process.exit(1); }
  const files = walk(ROOT);
  console.log(`Processing ${files.length} images...`);

  const map = {};
  for (const file of files) {
    const normalized = file.replace(/\\/g, "/");
    const key = "/images/products/" + normalized.replace(ROOT + "/", "");
    map[key] = await getEdgeColor(file);
    console.log(`  ${key} → ${map[key]}`);
  }

  const code = `// auto-generated by scripts/extract-edge-colors.mjs
const edgeColors: Record<string, string> = ${JSON.stringify(map, null, 2)};
export default edgeColors;
`;
  writeFileSync("src/data/edge-colors.ts", code, "utf-8");
  console.log(`\nWrote ${Object.keys(map).length} colors to src/data/edge-colors.ts`);
}

main().catch(console.error);
