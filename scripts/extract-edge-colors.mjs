import sharp from "sharp";
import { readdirSync, existsSync, writeFileSync } from "fs";
import { join } from "path";

const ROOT = "public/images/products";

function walk(dir) {
  const entries = readdirSync(dir, { withFileTypes: true });
  let files = [];
  for (const e of entries) {
    const full = join(dir, e.name);
    if (e.isDirectory()) files = files.concat(walk(full));
    else if (/\.(jpe?g|png)$/i.test(e.name)) files.push(full);
  }
  return files;
}

function mean(buf) {
  let r = 0, g = 0, b = 0, n = 0;
  for (let i = 0; i < buf.length; i += 4) {
    r += buf[i]; g += buf[i + 1]; b += buf[i + 2]; n++;
  }
  return n ? [Math.round(r / n), Math.round(g / n), Math.round(b / n)] : null;
}

function hex([r, g, b]) {
  return "#" + [r, g, b].map((c) => c.toString(16).padStart(2, "0")).join("");
}

function diff(c1, c2) {
  return Math.abs(c1[0] - c2[0]) + Math.abs(c1[1] - c2[1]) + Math.abs(c1[2] - c2[2]);
}

async function sample(file, left, top, w, h) {
  return await sharp(file).extract({ left, top, width: w, height: h }).ensureAlpha().raw().toBuffer();
}

async function getColors(file) {
  const { width: w, height: h } = await sharp(file).metadata();

  // corner squares — 5% of each dimension, clamped 10–60px
  const cs = Math.max(10, Math.min(60, Math.round(Math.min(w, h) * 0.05)));
  const cw = Math.min(cs, w), ch = Math.min(cs, h);

  const tl = mean(await sample(file, 0, 0, cw, ch));
  const tr = mean(await sample(file, w - cw, 0, cw, ch));
  const bl = mean(await sample(file, 0, h - ch, cw, ch));
  const br = mean(await sample(file, w - cw, h - ch, cw, ch));

  // thick edge strips — 10px
  const es = Math.min(10, w, h);
  const topE = mean(await sample(file, 0, 0, w, es));
  const botE = mean(await sample(file, 0, h - es, w, es));
  const leftE = mean(await sample(file, 0, es, es, h - 2 * es));
  const rightE = mean(await sample(file, w - es, es, es, h - 2 * es));

  // for the bg color: prefer corners (likely to be actual background)
  const corners = [tl, tr, bl, br].filter(Boolean);
  const have4Corners = corners.length === 4;

  // if 4 corners differ significantly, use gradient; else flat avg
  const maxDelta = 20;
  const hasGrad = have4Corners && (
    diff(tl, tr) > maxDelta || diff(tl, bl) > maxDelta ||
    diff(br, bl) > maxDelta || diff(br, tr) > maxDelta
  );

  // derive the color(s) for background
  let bgColors;
  if (hasGrad && have4Corners) {
    // 4-corner gradient
    bgColors = { tl: hex(tl), tr: hex(tr), bl: hex(bl), br: hex(br) };
  } else {
    // flat average of corners (or edges if corners failed)
    const source = have4Corners ? corners : [topE, botE, leftE, rightE].filter(Boolean);
    const avg = source.reduce((a, c) => [a[0] + c[0], a[1] + c[1], a[2] + c[2]], [0, 0, 0]);
    const n = source.length;
    bgColors = { flat: hex([Math.round(avg[0] / n), Math.round(avg[1] / n), Math.round(avg[2] / n)]) };
  }

  return {
    ...bgColors,
    gradient: hasGrad,
    top: hex(topE), bottom: hex(botE), left: hex(leftE), right: hex(rightE),
  };
}

async function main() {
  if (!existsSync(ROOT)) { console.error("not found"); process.exit(1); }
  const files = walk(ROOT);
  console.log(`Processing ${files.length} images...`);

  const map = {};
  for (const file of files) {
    const normalized = file.replace(/\\/g, "/");
    const key = "/images/products/" + normalized.replace(ROOT + "/", "");
    map[key] = await getColors(file);
    const c = map[key];
    console.log(`  ${key} → ${c.flat || `${c.tl}/${c.tr}/${c.bl}/${c.br}`} ${c.gradient ? "(gradient)" : ""}`);
  }

  const code = `// auto-generated by scripts/extract-edge-colors.mjs
export type EdgeColors =
  | { gradient: false; flat: string; top: string; bottom: string; left: string; right: string }
  | { gradient: true; tl: string; tr: string; bl: string; br: string; top: string; bottom: string; left: string; right: string };
const edgeColors: Record<string, EdgeColors> = ${JSON.stringify(map, null, 2)};
export default edgeColors;
`;
  writeFileSync("src/data/edge-colors.ts", code, "utf-8");
  console.log(`\nWrote ${Object.keys(map).length} entries to src/data/edge-colors.ts`);
}

main().catch(console.error);
