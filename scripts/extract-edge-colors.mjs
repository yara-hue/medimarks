import sharp from "sharp";
import { readdirSync, existsSync, writeFileSync } from "fs";
import { join } from "path";

const ROOT = "public/images/products";
const STOPS = 64;
const EDGE = 3; // sample only the outermost 3px

function walk(dir) {
  const entries = readdirSync(dir, { withFileTypes: true });
  let files = [];
  for (const e of entries) {
    const full = join(dir, e.name);
    if (e.isDirectory()) files = files.concat(walk(full));
    else if (/\.(jpe?g|png)$/i.test(e.name)) files.push(full);
  }
  return files;
}

function mean(buf) {
  let r = 0, g = 0, b = 0, n = 0;
  for (let i = 0; i < buf.length; i += 4) {
    r += buf[i]; g += buf[i + 1]; b += buf[i + 2]; n++;
  }
  return n ? [Math.round(r / n), Math.round(g / n), Math.round(b / n)] : null;
}

function hex([r, g, b]) {
  return "#" + [r, g, b].map((c) => c.toString(16).padStart(2, "0")).join("");
}

function diff(c1, c2) {
  return Math.abs(c1[0] - c2[0]) + Math.abs(c1[1] - c2[1]) + Math.abs(c1[2] - c2[2]);
}

// fraction t ∈ [0, 1) → (x, y, edge) around perimeter clockwise from top-left
function perimeterPoint(w, h, t) {
  const p = t * 2 * (w + h);
  if (p < w) return { x: p, y: 0, edge: "top" };
  if (p < w + h) return { x: w, y: p - w, edge: "right" };
  if (p < 2 * w + h) return { x: w - (p - w - h), y: h, edge: "bottom" };
  return { x: 0, y: h - (p - 2 * w - h), edge: "left" };
}

async function sample(file, left, top, w, h) {
  return await sharp(file).extract({ left, top, width: w, height: h }).ensureAlpha().raw().toBuffer();
}

async function getColors(file) {
  const { width: w, height: h } = await sharp(file).metadata();
  const half = Math.floor(EDGE / 2); // 1

  const colors = [];
  for (let i = 0; i < STOPS; i++) {
    const t = i / STOPS;
    const { x, y, edge } = perimeterPoint(w, h, t);

    let left, top, sw, sh;

    if (edge === "top") {
      // 3px inward from top, 3px wide along edge centered on x
      left = Math.max(0, Math.round(x - half));
      top = 0;
      sw = Math.min(EDGE, w - left);
      sh = Math.min(EDGE, h);
    } else if (edge === "right") {
      // 3px inward from right, 3px tall along edge centered on y
      left = w - EDGE;
      top = Math.max(0, Math.round(y - half));
      sw = EDGE;
      sh = Math.min(EDGE, h - top);
    } else if (edge === "bottom") {
      // 3px inward from bottom, 3px wide along edge centered on x
      left = Math.max(0, Math.round(x - half));
      top = h - EDGE;
      sw = Math.min(EDGE, w - left);
      sh = EDGE;
    } else {
      // left edge: 3px inward from left, 3px tall along edge centered on y
      left = 0;
      top = Math.max(0, Math.round(y - half));
      sw = EDGE;
      sh = Math.min(EDGE, h - top);
    }

    if (sw > 0 && sh > 0) {
      const buf = await sample(file, left, top, sw, sh);
      colors.push(mean(buf));
    }
  }

  const valid = colors.filter(Boolean);
  const sum = valid.reduce((a, c) => [a[0] + c[0], a[1] + c[1], a[2] + c[2]], [0, 0, 0]);
  const avg = [Math.round(sum[0] / valid.length), Math.round(sum[1] / valid.length), Math.round(sum[2] / valid.length)];

  let md = 0;
  for (let i = 0; i < valid.length; i++)
    for (let j = i + 1; j < valid.length; j++)
      md = Math.max(md, diff(valid[i], valid[j]));

  return { stops: valid.map(hex), flat: hex(avg), gradient: md > 15 };
}

async function main() {
  if (!existsSync(ROOT)) { console.error("not found"); process.exit(1); }
  const files = walk(ROOT);
  console.log(`Processing ${files.length} images...`);

  const map = {};
  for (const file of files) {
    const normalized = file.replace(/\\/g, "/");
    const key = "/images/products/" + normalized.replace(ROOT + "/", "");
    map[key] = await getColors(file);
    const c = map[key];
    console.log(`  ${key} → ${c.flat} ${c.gradient ? `(gradient, ${c.stops.length} stops)` : "(flat)"}`);
  }

  const code = `// auto-generated by scripts/extract-edge-colors.mjs
export type EdgeColors = {
  gradient: boolean;
  flat: string;
  stops: string[];
};
const edgeColors: Record<string, EdgeColors> = ${JSON.stringify(map, null, 2)};
export default edgeColors;
`;
  writeFileSync("src/data/edge-colors.ts", code, "utf-8");
  console.log(`\nWrote ${Object.keys(map).length} entries to src/data/edge-colors.ts`);
}

main().catch(console.error);
